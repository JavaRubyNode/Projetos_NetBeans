package potenciaeletrica;

/**
 *
 * @author VINICIUS
 */
public class Objetos {
   
    private double motor;
    private double volt1;
    private double volt2;
    private double watts;
    private double hp;
    private double cv;

    private double corrente1;
    private double corrente2;
    
    
    
    public double getMotor() {
        return motor;
    }

    public void setMotor(double motor) {
        this.motor = motor;
    }

    public double getVolt1() {
        return volt1;
    }

    public void setVolt1(double volt1) {
        this.volt1 = volt1;
    }

    public double getVolt2() {
        return volt2;
    }

    public void setVolt2(double volt2) {
        this.volt2 = volt2;
    }

    public double getWatts() {
        return watts;
    }

    public void setWatts(double watts) {
        this.watts = watts;
    }

    public double getHp() {
        return hp;
    }

    public void setHp(double hp) {
        this.hp = hp;
    }

    public double getCv() {
        return cv;
    }

    public void setCv(double cv) {
        this.cv = cv;
    }

    public double getCorrente1() {
        return corrente1;
    }

    public void setCorrente1(double corrente1) {
        this.corrente1 = corrente1;
    }

    public double getCorrente2() {
        return corrente2;
    }

    public void setCorrente2(double corrente2) {
        this.corrente2 = corrente2;
    }
    
}
