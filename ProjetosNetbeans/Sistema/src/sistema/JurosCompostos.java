    private void b_jurosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_jurosActionPerformed
        
        capit2 = Double.parseDouble(campo_capital2.getText());
        tempo2 = Integer.parseInt(campo_tempo2.getText());
        taxa2 = Double.parseDouble(campo_taxa2.getText());
        
        y =((taxa2/100)+1);
       
        juros = capit2*(Math.pow(y,tempo2)-1);
        
        texto.setText(" Para Juros Compostos usamos a seguinte formula : "+
                "\n J =  C . (( 1 + I )-1) elevando ao T "+
                "\n J = juros, C = capital , I = taxa , T = tempo "+
                "\n ---------------------------------------------------"+
                "\n J = "+capit2+" .  (( 1 + "+taxa2+" ) -1 ) elevado T "+
                "\n J = "+capit2+" .  (( "+y+" ) -1 ) elevado T "+
                "\n J = "+capit2+" .  "+(Math.pow(y,tempo2)-1)+
                "\n J = "+juros+
                "\n ----------------------------------------------------"+
                "\n Arredondando o valor fica J = R$ "+Math.round(juros));
        
                
        
        b_limpar.setEnabled(true);
        
    }//GEN-LAST:event_b_jurosActionPerformed


