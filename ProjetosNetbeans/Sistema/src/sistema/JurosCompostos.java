    private void b_montanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_montanteActionPerformed
      
        capit = Double.parseDouble(campo_capital.getText());
        taxa = Double.parseDouble(campo_taxa.getText());
        tempo = Integer.parseInt(campo_tempo.getText());
       
        x = ((taxa/100)+1); 
        
        montante = ( capit*Math.pow(x, tempo));
        
        texto.setText(" Para Juros Compostos usamos a seguinte formula : "+
                "\n M = C . ( 1 + I ) elevando ao T "+
                "\n M = montante , C = capital , I = taxa , T = tempo "+
                "\n -------------------------------------------------"+
                "\n M = "+capit+" . ( 1 + "+taxa+" % ) elevando T "+
                "\n M = "+capit+" . ( "+x+" ) elevando T "+
                "\n M = "+montante+
                "\n ----------------------------------------------------"+
                "\n Arredondando o valor M = R$ "+Math.round(montante)); 
       
        b_limpar.setEnabled(true);
        
    }//GEN-LAST:event_b_montanteActionPerformed

